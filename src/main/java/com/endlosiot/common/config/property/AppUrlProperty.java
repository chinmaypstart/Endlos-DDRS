package com.endlosiot.common.config.property;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.beans.factory.annotation.Value;
import org.springframework.stereotype.Component;

/**
 * This class configures environment related properties.
 *
 * @author nirav
 */
@Component
public class AppUrlProperty {

    private final String appUrl;
    private final String registrationAppUrl;
    private final boolean apiHostedOnDifferentDomain;
    private final String allowDomains;
    private final String apiUrl;
    private final boolean isWeeklyStatusJobEnabled;

    @Autowired
    public AppUrlProperty(@Value("${app.url}") String appUrl,
                          @Value("${registration.app.url}") String registrationAppUrl,
                          @Value("${api.hosted.on.different.domain}") boolean apiHostedOnDifferentDomain,
                          @Value("${allow.domains}") String allowDomains, @Value("${api.url}") String apiUrl,
                          @Value("${weeklystatus.job.enable}") boolean isWeeklyStatusJobEnabled) {
        this.appUrl = appUrl;
        this.registrationAppUrl = registrationAppUrl;
        this.apiHostedOnDifferentDomain = apiHostedOnDifferentDomain;
        this.allowDomains = allowDomains;
        this.apiUrl = apiUrl;
        this.isWeeklyStatusJobEnabled = isWeeklyStatusJobEnabled;
    }

    public String getAppUrl() {
        return appUrl;
    }

    public String getRegistrationAppUrl() {
        return registrationAppUrl;
    }

    public boolean isApiHostedOnDifferentDomain() {
        return apiHostedOnDifferentDomain;
    }


































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































    

    public String getAllowDomains() {
        return allowDomains;
    }

    public String getApiUrl() {
        return apiUrl;
    }

    public boolean isWeeklyStatusJobEnabled() {
        return isWeeklyStatusJobEnabled;
    }
}